/* convert RGB(8bit, 8bit, 8bit) to RGB(5bit, 6bit, 5bit) */
#define RGB565(r, g, b) (((r & 0b11111000) << 8) | ((g & 0b11111100) << 3) | (b >> 3))
/* extract colors from RGB(5bit, 6bit, 5bit) */
#define RGB565_R(c) (((((c >> 11) & 0x1F) * 527) + 23) >> 6)
#define RGB565_G(c) (((((c >> 5) & 0x3F) * 259) + 33) >> 6)
#define RGB565_B(c) ((((c & 0x1F) * 527) + 23) >> 6)
/* convert color ranges */
#define RGB_0_255(c) (c * 255))
#define RGB_0_1(c)   (c * (1.0f / 255.0f))

